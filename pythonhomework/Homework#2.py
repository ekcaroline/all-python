int_first = int(input("Enter an integer: "))
int_second = int(input("Enter another integer: "))

int_product = int_first * int_second
print(int_first,"*", int_second, "=", int_product)

float_div = int_first / int_second 
print(int_first, "/", int_second, "=", format(float_div, '.2f'))

int_div = int_first // int_second
print(int_first, "//", int_second, "=", int_div)

int_remainder = int_first % int_second
print(int_first, "%", int_second, "=", int_remainder)

int_sum = int_first + int_second
print(int_first, "+", int_second, "=", int_sum)

int_diff = int_first - int_second
print(int_first, "-", int_second, "=", int_diff)




